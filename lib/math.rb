def addition(num1, num2)
  sum = num1 + num2
  sum
end

def subtraction(num1, num2)
  subt = num1 - num2
  subt
end

def division(num1, num2)
  div = num1 / num2
  div
end

def multiplication(num1, num2)
  mult = num1 * num2
  mult
end

def modulo(num1, num2)
  mod = num1 % num2
  mod
end

def square_root(num)
  sqr = num ** 0.5
  sqr
end